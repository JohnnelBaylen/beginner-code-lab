package operators;

public class Arithmetic {
    public static void main(String[] args) {

        // declare variables
        int a = 10, b = 5;

        // addition 
        System.out.println("a + b = " + (a + b));

        // subtraction
        System.out.println("a - b = " + (a - b));

        // multiplication
        System.out.println("a x b = " + (a * b));

        // division
        System.out.println("a / b = " + (a / b));

        // modulo
        System.out.println("a % b = " + (a % b));
    }
}
